// Os loops permitem repetir um bloco de código várias vezes. O Swift oferece diferentes
// tipos de loops:
// • for-in: Ideal para iterar sobre coleções ou intervalos de números.
// • while: Executa enquanto uma condição for verdadeira.
// • repeat-while: Executa pelo menos uma vez e depois verifica a condição.

for i in 1...10 {
    print(i)
}