// Um dicionário em Swift é uma coleção não ordenada de pares chave-valor. Ele é útil
// para armazenar dados que podem ser recuperados rapidamente por uma chave única.
// • [:]: Usado para definir um dicionário.
// • Chave e valor: Cada elemento do dicionário é um par chave-valor.

var paises = ["BR": "Brasil", "EUA": "Estados Unidos", "ARG": "Argentina", "FRA": "França"]

print(paises["BR"]!) // Brasil