// As condicionais permitem executar um bloco de código com base em uma condição. O
// if/else é usado para tomar decisões, verificando se a condição é verdadeira ou falsa.
// • if: Avalia uma condição.
// • else: Define o que acontecerá quando a condição não for verdadeira.
// • else if: Usado quando há mais de uma condição a ser verificada.

let numero = 5

if numero == 0 {
    print("O número e 0")
} else if numero == 1 {
    print("O número e 1")
} else {
    print("O número não é 0 e 1")
}