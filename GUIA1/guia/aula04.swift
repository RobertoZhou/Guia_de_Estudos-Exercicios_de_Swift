// As funções em Swift são blocos de código que podem ser chamados para executar
// uma tarefa específica. Elas podem receber parâmetros e retornar valores.
// • func: Palavra-chave usada para declarar uma função.
// • Parâmetros: Valores passados para a função.
// • Retorno: A função pode retornar um valor com a palavra-chave return

func adicionaUm(_ x: Int) -> Int {
    return x + 1
}
