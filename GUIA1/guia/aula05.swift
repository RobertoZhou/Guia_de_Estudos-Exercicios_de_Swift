// Um array é uma coleção ordenada de elementos do mesmo tipo. Os elementos podem
// ser acessados usando índices. Swift fornece várias operações para adicionar, remover
// e acessar elementos em um array.
// • []: Usado para definir um array.
// • append(): Método para adicionar elementos a um array.
// • Índice: Os índices começam em 0.

// Criando um array de nomes
let nomes = ["Alice", "Bob", "Carlos", "Diana", "Eduarda"]

// Acessando e imprimindo elementos do array
print(nomes[0]) // Imprime o primeiro nome: "Alice"