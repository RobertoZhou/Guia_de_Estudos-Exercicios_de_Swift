// O switch case permite testar várias condições de forma eficiente. Ele pode ser usado
// para verificar intervalos, valores específicos, ou condições complexas.
// • case: Define um valor ou condição a ser verificada.
// • default: Caso nenhum case seja verdadeiro.


let nome = "Mark"

switch nome {
    case "Mark":
        print("Olá, Mark!")
    case "João":
        print("Olá, João!")
    default:
        print("Olá, Estranho!")
}
